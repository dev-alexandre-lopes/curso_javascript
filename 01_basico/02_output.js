/* Literal:
Um literal é um valor de dados que aparece diretamente em um programa. 
A seguir estão todos os literais:

12              //Número 12
1.2             //Número 1.2
"hello world"   //Uma String
'Hi'            //Outra String
true            //Valor Booleano
false           //Valor Booleano
null            //Ausência de um objeto
*/


/*Identificador:
Um identificador é simplesmente um nome. Em JavaScript, os identificadores são usados ​​
para nomear constantes, variáveis, propriedades, funções e classes e para fornecer rótulos 
para certos loops no código JavaScript. Um identificador JavaScript deve começar com uma 
letra, um sublinhado (_) ou um cifrão ($). 

Os caracteres subsequentes podem ser letras, dígitos, sublinhados ou cifrões. 
(Dígitos não são permitidos como o primeiro caractere para que o JavaScript possa 
facilmente distinguir identificadores de números.)
*/

