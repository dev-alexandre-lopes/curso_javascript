/* Exercício 01:
Digite dois números, realize a operação de adição e apresente o resultado com a função alert
*/

let number1 = prompt('Digite o primeiro número:');
let number2 = prompt('Digite o segundo número:');
number1 = parseFloat(number1);
number2 = parseFloat(number2);
const resultado =  (number1 + number2);
alert(`O resultado da soma é: ${resultado}`);
